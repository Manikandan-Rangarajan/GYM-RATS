import mongoose from "mongoose";

// Client Schema
const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  }
});


const Client = mongoose.model('Client', UserSchema);

export { Client};